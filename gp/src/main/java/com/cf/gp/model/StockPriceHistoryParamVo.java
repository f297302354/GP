package com.cf.gp.model;

public class StockPriceHistoryParamVo {

	private String code;
	
	private int dayAvg;
	
	private String date;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public int getDayAvg() {
		return dayAvg;
	}

	public void setDayAvg(int dayAvg) {
		this.dayAvg = dayAvg;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	
}
